package network.xyo.sdkcorekotlin.data.heuristics.number

/**
 * The class is used to show all of the options when encoding numbers.
 */
enum class XyoNumberTypes {
    /**
     * 8 bits
     */
    BYTE,

    /**
     * 2 bytes
     */
    SHORT,

    /**
     * 4 bytes
     */
    INT,

    /**
     * 8 bytes
     */
    LONG,

    /**
     * 4 bytes
     */
    FLOAT
}